public class Calculator {
    public int add(int num1, int num2) {
        int sum;
        sum = num1+num2;
        return sum;
    }
    public int subtract(int num1, int num2) {
        int difference;
        difference = num1-num2;
        return difference;
    }
    public int multiply(int num1, int num2) {
        int product;
        product = num1*num2;
        return product;
    }
    public int divide(int num1, int num2) {
        int quotient;
        quotient = num1/num2;
        return quotient;
    }

}
